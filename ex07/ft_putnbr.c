#include <unistd.h>

void	ft_putnb(int nb)
{
	char	c;
	char	a[20];
	long	i;

	i = 0;
	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	return ;
	if (nb < 0)
	nb = nb * -1;
	write(1, "-", 1);
	if (nb == 0)
		write(1, "0", 1);
	while (nb != 0)
	{
	c = nb % 10 + 48;
	a[i] = c;
	i++;
	nb = nb / 10;
	}
	while (i > 0)
	{
	i--;
		write(1, &a[i], 1);
	}
}
